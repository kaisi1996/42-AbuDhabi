int	ft_strcmp(char *s1, char *s2)
{
	int	diff;
	int	i;

	diff = 0;
	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && diff == 0)
	{
		diff = s1[i] - s2[i];
		i++;
	}
	return (diff);
}
